/** Western digits вҶ’ Khmer numerals. */
export function toKh(input: string | number): string {
  const kh = ['бҹ ', 'бҹЎ', 'бҹў', 'бҹЈ', 'бҹӨ', 'бҹҘ', 'бҹҰ', 'бҹ§', 'бҹЁ', 'бҹ©'];
  return String(input).replace(/[0-9]/g, (d) => kh[Number(d)]);
}

