def merge_json(en_path, km_path):
    with open(en_path, 'r', encoding='utf-8') as f:
        en_data = json.load(f)
    if os.path.exists(km_path):
        with open(km_path, 'r', encoding='utf-8') as f:
            km_data = json.load(f)
    else:
        km_data = {}

    stats = {"auto": 0, "explicit": 0}

    def walk(en_obj, km_obj, path=""):
        for key, value in en_obj.items():
            current_path = f"{path}.{key}" if path else key
            
            # Handle explicit keys
            if current_path in EXPLICIT_KM:
                km_obj[key] = EXPLICIT_KM[current_path]
                stats["explicit"] += 1
            
            # Handle auto keys or missing keys
            if key not in km_obj or km_obj[key] == value:
                if isinstance(value, str):
                    if key.startswith('auto.') or path.startswith('auto.'):
                        if value in DICTIONARY:
                            km_obj[key] = DICTIONARY[value]
                            stats["auto"] += 1
                    # Also try for non-auto if they are same as English and in dictionary
                    elif value in DICTIONARY and km_obj.get(key) == value:
                         km_obj[key] = DICTIONARY[value]
                         stats["auto"] += 1

            if isinstance(value, dict):
                if key not in km_obj:
                    km_obj[key] = {}
                walk(value, km_obj[key], current_path)

    walk(en_data, km_data)
    
    with open(km_path, 'w', encoding='utf-8') as f:
        json.dump(km_data, f, ensure_ascii=False, indent=2)
    
    return stats

