async function createUniversalKnowledgePosts() {
  const targetEmail = 'naing.seiha.hs@moeys.gov.kh';
  console.log(`рҹ”Қ Finding user with email: ${targetEmail}...`);
  
  const user = await prisma.user.findUnique({
    where: { email: targetEmail },
  });

  if (!user) {
    console.error(`вқҢ User with email ${targetEmail} not found!`);
    return;
  }

  console.log(`рҹҡҖ Creating ${postsData.length} Universal Knowledge Posts...`);

  for (const post of postsData) {
    const createdPost = await prisma.post.create({
      data: {
        authorId: user.id,
        title: post.title,
        content: post.content,
        postType: post.postType,
        visibility: 'PUBLIC',
        topicTags: post.topicTags,
        mediaUrls: post.mediaUrls,
        mediaMetadata: { 
          images: post.mediaUrls.map(() => ({ width: 1000, height: 1500 })) 
        },
        mediaAspectRatio: post.mediaAspectRatio,
        mediaDisplayMode: 'AUTO',
        difficultyLevel: 2.0,
      },
    });
    console.log(`вң… Created Post: ${post.title} (ID: ${createdPost.id}) with ${post.mediaUrls.length} images.`);
  }

  console.log('\nвңЁ All universal knowledge posts created successfully!');
}

